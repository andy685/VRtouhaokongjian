def set_cn_font(run, name='宋体', size=10.5, bold=False, color=None):
    run.font.name = name
    run._element.rPr.rFonts.set(qn('w:eastAsia'), name)
    run.font.size = Pt(size)
    run.font.bold = bold
    if color:
        run.font.color.rgb = color

PLACEHOLDER_COLOR = RGBColor(0xC0, 0x00, 0x00)  # 深红，醒目

def add_title(text, size=18):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(text)
    set_cn_font(r, '黑体', size, bold=True)
    p.paragraph_format.space_after = Pt(6)
    return p

def add_subtitle(text, size=12):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(text)
    set_cn_font(r, '宋体', size, bold=False, color=RGBColor(0x40,0x40,0x40))
    p.paragraph_format.space_after = Pt(12)
    return p

def add_clause(text, size=10.5, bold=False):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_cn_font(r, '宋体', size, bold=bold)
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(2)
    return p

def add_clause_ph(segments):
    """segments: list of (text, is_placeholder)"""
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(2)
    for text, is_ph in segments:
        r = p.add_run(text)
        if is_ph:
            set_cn_font(r, '宋体', 10.5, bold=True, color=PLACEHOLDER_COLOR)
        else:
            set_cn_font(r, '宋体', 10.5)
    return p

def add_heading(text, level=1):
    sizes = {1: 14, 2: 12, 3: 11}
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_cn_font(r, '黑体', sizes.get(level, 11), bold=True)
    p.paragraph_format.space_before = Pt(10)
    p.paragraph_format.space_after = Pt(4)
    return p

def add_table(headers, rows, widths=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Table Grid'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr = t.rows[0].cells
    for i, h in enumerate(headers):
        hdr[i].text = ''
        p = hdr[i].paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(h)
        set_cn_font(r, '黑体', 10, bold=True)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            cells[i].text = ''
            p = cells[i].paragraphs[0]
            r = p.add_run(val)
            # 占位符着色
            if val.startswith('【') and val.endswith('】'):
                set_cn_font(r, '宋体', 10, bold=True, color=PLACEHOLDER_COLOR)
            else:
                set_cn_font(r, '宋体', 10)
    if widths:
        for i, w in enumerate(widths):
            for row in t.rows:
                row.cells[i].width = Cm(w)
    doc.add_paragraph()
    return t

